              <h2 class="text-center">Descrição Detalhada</h2>
              <p>
                Este sistema foi desenvolvido para automatizar o processo de avaliações de desempenho e clima organizacional (modelo 360°), transformando dados brutos de questionários em relatórios visuais e informativos. A ferramenta otimiza o trabalho de gestores e do RH, fornecendo insights claros sobre competências individuais e de equipes.
              </p>
              <h4 class="text-center">Arquitetura e Fluxo de Trabalho</h4>
              <ol>
                <li><strong>Gestão da Avaliação:</strong> A plataforma permite o cadastro de colaboradores (<code>cadastrar_colaborador.php</code>) e a criação de questionários personalizados (<code>perguntas.php</code>).</li>
                <li><strong>Coleta de Respostas:</strong> Os funcionários acessam o sistema para responder às avaliações através de uma interface segura (<code>login.php</code>).</li>
                <li><strong>Processamento e Análise:</strong> O back-end em PHP processa as respostas e consolida os dados no banco de dados MySQL, preparando-os para a visualização.</li>
                <li><strong>Visualização e Exportação:</strong> Um dashboard (<code>dashboard.php</code>) permite a consulta detalhada dos resultados. O sistema utiliza Chart.js para renderizar gráficos dinâmicos de desempenho por colaborador (<code>grafico_avaliacao_colaborador.php</code>) e por setor (<code>grafico_avaliacao_setor.php</code>). Finalmente, a biblioteca Dompdf é utilizada para gerar relatórios profissionais em PDF, combinando tabelas e os gráficos gerados.</li>
              </ol>
            </div>

            <div class="portfolio-description mt-4" data-aos="fade-up" data-aos-delay="400">
                <h4 class="text-center">Principais Funcionalidades Implementadas</h4>
                <ul>
                    <li><strong>Dashboard Interativo:</strong> Painel central para visualização de resultados gerais e navegação pelas análises (<code>dashboard.php</code>).</li>
                    <li><strong>Geração de Gráficos Dinâmicos:</strong> Criação de visualizações de dados por colaborador, setor e competências utilizando Chart.js.</li>
                    <li><strong>Exportação para PDF com Dompdf:</strong> Geração de relatórios completos e customizados em PDF, prontos para impressão ou compartilhamento.</li>
                    <li><strong>Exportação para Excel:</strong> Funcionalidade adicional para exportar os dados brutos para planilhas (<code>exportar_excel.php</code>), permitindo análises mais aprofundadas.</li>
                    <li><strong>Sistema de Autenticação:</strong> Controle de acesso seguro para garantir a confidencialidade das informações de avaliação.</li>
                    <li><strong>Gestão de Colaboradores e Perguntas:</strong> Módulos administrativos para manter a base de usuários e os questionários sempre atualizados.</li>
